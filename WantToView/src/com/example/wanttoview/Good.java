package com.example.wanttoview;

public class Good {

	private String mTitle;
	private String mPrice;
	private String mManufacturer;
	private String mScreenSize;
	private String mScreenResolution;
	
	public String getTitle() {
		return mTitle;
	}
	public void setTitle(String mTitle) {
		this.mTitle = mTitle;
	}
	public String getPrice() {
		return mPrice;
	}
	public void setPrice(String mPrice) {
		this.mPrice = mPrice;
	}
	public String getManufacturer() {
		return mManufacturer;
	}
	public void setManufacturer(String mManufacturer) {
		this.mManufacturer = mManufacturer;
	}
	public String getScreenSize() {
		return mScreenSize;
	}
	public void setScreenSize(String mScreenSize) {
		this.mScreenSize = mScreenSize;
	}
	public String getScreenResolution() {
		return mScreenResolution;
	}
	public void setScreenResolution(String mScreenResolution) {
		this.mScreenResolution = mScreenResolution;
	}
	
}
